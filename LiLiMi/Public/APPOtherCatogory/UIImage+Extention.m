/**
 *  拉伸图片：从中心拉伸
 *
 *
 *  @return 拉伸后的图片
 */
+ (UIImage *)resizeImage:(NSString *)imgName
{
    UIImage *img = [UIImage imageNamed:imgName];
    return [img stretchableImageWithLeftCapWidth:img.size.width / 2 topCapHeight:img.size.height / 2];
}
/**
 *  保存图片---tmp
 *
 *  @param imageName 名字
 *
 *  @return yes or no
 */
- (BOOL)saveImageLocalDirTmpWithImageName:(NSString *)imageName{
    NSString* imagePath = [imageName appendTempDir];
    NSData* imageData = UIImagePNGRepresentation(self);
    NSFileManager* manager = [NSFileManager defaultManager];
    return [manager createFileAtPath:imagePath contents:imageData attributes:nil];
}
